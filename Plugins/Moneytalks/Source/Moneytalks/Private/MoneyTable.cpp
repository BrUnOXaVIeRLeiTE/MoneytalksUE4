FString FCurrencySymbol(const UDataTable* DT, const FName Code, const int32 Max) {
	if (!DT) {return TEXT("");}
	//
	int32 Row;
	FCurrencySymbols* Data;
	//
	for (Row = 0; Row<=Max; Row++) {
		Data = DT->FindRow<FCurrencySymbols>(*FString::FromInt(Row),TEXT("GENERAL"));
		//
		if (Data && Data->Code == Code) {
			return Data->Symbol.ToString(); break;
		}///
	} return TEXT("");
}

